package autoload

import (
	"bytes"
	"crypto/md5"
	"encoding/hex"
	"fmt"
	"text/template"
)

// renderData carries every value referenced by any template.
type renderData struct {
	InitClass string              // e.g. "ComposerAutoloaderInitabc..."
	Hash      string              // 32-hex chars
	PSR4      map[string][]string // prefix -> []relativeDir (trailing slash)
	// SortedPSR4 is supplied alongside PSR4 so templates iterate in
	// deterministic order without resorting on each render.
	SortedPSR4 []string
	// PSR4ByFirstChar groups SortedPSR4 by first character for use in
	// $prefixLengthsPsr4 in autoload_static.php. Ordered by first char.
	PSR4ByFirstChar []firstCharGroup
	Files           []FileEntry       // ordered, deduped, ready to emit
	Classmap        map[string]string // qualified-name -> project-relative path
	SortedClasses   []string          // sorted keys of Classmap, supplied for determinism
}

// fileKey is the deterministic hash key Composer uses in autoload_files
// arrays: md5(packageName . ":" . relativePath). For the root manifest
// (PackageName == "") Composer hashes md5(absolute baseDir + path); we
// stand in with md5("__root__:" + path) so two checkouts of the same
// project on different machines still produce the same key. The bootstrap
// only ever uses the keys for `isset($GLOBALS[...][...])` dedup so any
// stable string works.
func fileKey(f FileEntry) string {
	owner := f.PackageName
	if owner == "" {
		owner = "__root__"
	}
	sum := md5.Sum([]byte(owner + ":" + f.Path))
	return hex.EncodeToString(sum[:])
}

// firstCharGroup is one bucket in $prefixLengthsPsr4: all PSR-4 prefixes
// that share the same first character, in sorted order.
type firstCharGroup struct {
	Char     string   // single character, e.g. "A"
	Prefixes []string // sorted prefixes starting with Char
}

// autoloadPHPTpl is the canonical entrypoint that user code includes via
// `require 'vendor/autoload.php'`. We track Composer's exact bytes here.
const autoloadPHPTpl = `<?php

// autoload.php @generated by gomposer

require_once __DIR__ . '/composer/autoload_real.php';

return {{.InitClass}}::getLoader();
`

// autoloadRealTpl defines the init class. It now includes the
// composerRequire<HASH> helper and the $includeFiles loop for the `files`
// autoloader, matching Composer's post-2.0 bootstrap shape.
const autoloadRealTpl = `<?php

// autoload_real.php @generated by gomposer

class {{.InitClass}}
{
    private static $loader;

    public static function loadClassLoader($class)
    {
        if ('Composer\Autoload\ClassLoader' === $class) {
            require __DIR__ . '/ClassLoader.php';
        }
    }

    /**
     * @return \Composer\Autoload\ClassLoader
     */
    public static function getLoader()
    {
        if (null !== self::$loader) {
            return self::$loader;
        }

        spl_autoload_register(array('{{.InitClass}}', 'loadClassLoader'), true, true);
        self::$loader = $loader = new \Composer\Autoload\ClassLoader(\dirname(__DIR__));
        spl_autoload_unregister(array('{{.InitClass}}', 'loadClassLoader'));

        $useStaticLoader = PHP_VERSION_ID >= 50600 && !defined('HHVM_VERSION');
        if ($useStaticLoader) {
            require __DIR__ . '/autoload_static.php';
            call_user_func(\Composer\Autoload\ComposerStaticInit{{.Hash}}::getInitializer($loader));
        } else {
            $map = require __DIR__ . '/autoload_namespaces.php';
            foreach ($map as $namespace => $path) {
                $loader->set($namespace, $path);
            }

            $map = require __DIR__ . '/autoload_psr4.php';
            foreach ($map as $namespace => $path) {
                $loader->setPsr4($namespace, $path);
            }

            $classMap = require __DIR__ . '/autoload_classmap.php';
            if ($classMap) {
                $loader->addClassMap($classMap);
            }
        }

        $loader->register(true);

        $includeFiles = $useStaticLoader ? \Composer\Autoload\ComposerStaticInit{{.Hash}}::$files : require __DIR__ . '/autoload_files.php';
        foreach ($includeFiles as $fileIdentifier => $file) {
            composerRequire{{.Hash}}($fileIdentifier, $file);
        }

        return $loader;
    }
}

function composerRequire{{.Hash}}($fileIdentifier, $file)
{
    if (empty($GLOBALS['__composer_autoload_files'][$fileIdentifier])) {
        $GLOBALS['__composer_autoload_files'][$fileIdentifier] = true;

        require $file;
    }
}
`

// autoloadPSR4Tpl writes the prefix -> [absolute-path, ...] map.
// We use $vendorDir/$baseDir constants to mirror Composer's exact shape.
const autoloadPSR4Tpl = `<?php

// autoload_psr4.php @generated by gomposer

$vendorDir = dirname(__DIR__);
$baseDir = dirname($vendorDir);

return array(
{{- range $prefix := .SortedPSR4}}
    {{phpString $prefix}} => array({{range $i, $dir := index $.PSR4 $prefix}}{{if $i}}, {{end}}{{phpDir $dir}}{{end}}),
{{- end}}
);
`

// autoloadNamespacesTpl is always empty in Stage 1 (PSR-0 deferred).
const autoloadNamespacesTpl = `<?php

// autoload_namespaces.php @generated by gomposer

$vendorDir = dirname(__DIR__);
$baseDir = dirname($vendorDir);

return array(
);
`

const autoloadClassmapTpl = `<?php

// autoload_classmap.php @generated by gomposer

$vendorDir = dirname(__DIR__);
$baseDir = dirname($vendorDir);

return array(
{{- range $name := .SortedClasses}}
    {{phpString $name}} => {{phpDir (index $.Classmap $name)}},
{{- end}}
);
`

const autoloadFilesTpl = `<?php

// autoload_files.php @generated by gomposer

$vendorDir = dirname(__DIR__);
$baseDir = dirname($vendorDir);

return array(
{{- range $f := .Files}}
    {{phpString (fileKey $f)}} => {{phpDir $f.Path}},
{{- end}}
);
`

// autoloadStaticTpl mirrors Composer's --optimize-autoloader output. It
// includes $files (for the `files` autoloader) and $classMap (for classmap).
const autoloadStaticTpl = `<?php

// autoload_static.php @generated by gomposer

namespace Composer\Autoload;

class ComposerStaticInit{{.Hash}}
{
    public static $files = array(
{{- range $f := .Files}}
        {{phpString (fileKey $f)}} => {{phpStaticDir $f.Path}},
{{- end}}
    );

    public static $prefixLengthsPsr4 = array(
{{- range .PSR4ByFirstChar}}
        {{phpString .Char}} => array({{range $i, $p := .Prefixes}}{{if $i}}, {{end}}{{phpString $p}} => {{len $p}}{{end}}),
{{- end}}
    );

    public static $prefixDirsPsr4 = array(
{{- range $prefix := .SortedPSR4}}
        {{phpString $prefix}} => array({{range $i, $dir := index $.PSR4 $prefix}}{{if $i}}, {{end}}{{phpStaticDir $dir}}{{end}}),
{{- end}}
    );

    public static $classMap = array(
{{- range $name := .SortedClasses}}
        {{phpString $name}} => {{phpStaticDir (index $.Classmap $name)}},
{{- end}}
    );

    public static function getInitializer(ClassLoader $loader)
    {
        return \Closure::bind(function () use ($loader) {
            $loader->prefixLengthsPsr4 = ComposerStaticInit{{.Hash}}::$prefixLengthsPsr4;
            $loader->prefixDirsPsr4 = ComposerStaticInit{{.Hash}}::$prefixDirsPsr4;
            $loader->classMap = ComposerStaticInit{{.Hash}}::$classMap;
        }, null, ClassLoader::class);
    }
}
`

